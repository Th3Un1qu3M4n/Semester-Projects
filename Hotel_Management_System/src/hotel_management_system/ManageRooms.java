    private void jButtonAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewActionPerformed
        
            
        String roomNumberString = jRoomIdField.getText();
        int roomType = Integer.valueOf(jTypeComboBox.getSelectedItem().toString());
        String phoneNumber = jPhoneNumber.getText();
            
        if(roomNumberString.trim().equals(""))
            JOptionPane.showMessageDialog(this, "Client id cannot be Empty!", "Room Id ERROR", JOptionPane.ERROR_MESSAGE);
        else{
        
        int roomNumber = Integer.valueOf(roomNumberString);
                 
        
        if(rooms.addRooms(roomNumber, roomType, phoneNumber)) {            
            JOptionPane.showMessageDialog(this, "Room has been Added Successfully!", "Added Successfully!",  JOptionPane.INFORMATION_MESSAGE);
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Room Number","Room Type","Phone Number", "Is Reserved"}));
            rooms.refreshRoomTable(jTable1);
            
        } else {
            JOptionPane.showMessageDialog(this, "Could not Add the Room!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButtonAddNewActionPerformed

