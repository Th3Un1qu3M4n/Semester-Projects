    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        
        String roomNumberString = jRoomIdField.getText();
        int roomType = Integer.valueOf(jTypeComboBox.getSelectedItem().toString());
        String phoneNumber = jPhoneNumber.getText();
        String reserved = "";
                
        if(jYesRadioButton.isSelected())
            reserved = "Yes";
        else
            reserved = "No";
            
        if(roomNumberString.trim().equals(""))
            JOptionPane.showMessageDialog(this, "Client id cannot be Empty!", "Room Id ERROR", JOptionPane.ERROR_MESSAGE);
        else{
        
        int roomNumber = Integer.valueOf(roomNumberString);
                 
        
        if(rooms.updateRoom(roomNumber, roomType, phoneNumber, reserved)) {            
            JOptionPane.showMessageDialog(this, "Room has been Updated Successfully!", "Updated Successfully!",  JOptionPane.INFORMATION_MESSAGE);
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Room Number","Room Type","Phone Number", "Is Reserved"}));
            rooms.refreshRoomTable(jTable1);
            
        } else {
            JOptionPane.showMessageDialog(this, "Could not Update the Room!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        
        String stringId = jRoomIdField.getText();
        
        if(stringId.trim().equals(""))
            JOptionPane.showMessageDialog(this, "Room id cannot be Empty!", "Room Id ERROR", JOptionPane.ERROR_MESSAGE);
        else{
        
        int roomId = Integer.valueOf(stringId);
                 
        
        if(rooms.removeRoom(roomId)) {            
            JOptionPane.showMessageDialog(this, "Room has been Removed Successfully!", "Removed Successfully!",  JOptionPane.INFORMATION_MESSAGE);
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Room Number","Room Type","Phone Number", "Is Reserved"}));
            rooms.refreshRoomTable(jTable1);
        
        } else {
            JOptionPane.showMessageDialog(this, "Could not Remove the Room!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_jButtonRemoveActionPerformed

