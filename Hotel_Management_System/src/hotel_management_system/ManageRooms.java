    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        
        int rowIndex = jTable1.getSelectedRow();
        
        jRoomIdField.setText(tableModel.getValueAt(rowIndex,0).toString());
        jTypeComboBox.setSelectedIndex(Integer.valueOf(tableModel.getValueAt(rowIndex,1).toString())-1);
        jPhoneNumber.setText(tableModel.getValueAt(rowIndex,2).toString());
        String reserved = tableModel.getValueAt(rowIndex,3).toString();
        if(reserved.trim().equals("Yes"))
            jYesRadioButton.setSelected(true);
        else
            jNoRadioButton.setSelected(true);
    }//GEN-LAST:event_jTable1MouseClicked

